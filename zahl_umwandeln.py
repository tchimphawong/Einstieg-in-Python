#Positive Zahl
x = 12/7
print("x:", x)

#Rundung und Konvertierung
rx = round(x,3)
print("x gerundet auf drei Stellen: ", rx)
rx = round(x)
print("x gerundet auf null Stellen: ", rx)
ix = int(x)
print("int(x):", ix)
print()

