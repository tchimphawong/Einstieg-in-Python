# Kombinierte Zuweisungsoperatoren für Zahlen
x = 12
print(x)
x += 3
print(x)
x -= 9
print(x)
x **= 2
print(x)
x *= 3
print(x)
x //= 7
print(x)
x /= 4
print(x)
x %= 2
print(x)

